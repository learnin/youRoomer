package com.github.learnin.youroomer;

public class MenuItem {

	private int id;
	private String text;

	/**
	 * idを取得します。
	 *
	 * @return id
	 */
	public int getId() {
		return id;
	}

	/**
	 * idを設定します。
	 *
	 * @param id id
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * textを取得します。
	 *
	 * @return text
	 */
	public String getText() {
		return text;
	}

	/**
	 * textを設定します。
	 *
	 * @param text text
	 */
	public void setText(String text) {
		this.text = text;
	}

}
